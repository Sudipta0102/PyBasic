import numpy as np

# 1. an array class in numpy called ndarray.
# 2. In numpy, dimensions of the array is called as rank of the array.


# creating an array from the list
# rank 1 arr
arr = np.array([1, 2, 3])
print(arr)

# rank 2 arr
arr = np.array([[1, 2, 3], [4, 5, 6]])
print(arr)


# creating an array from the tuple
arr = np.array((1, 3, 2))
print(arr)


