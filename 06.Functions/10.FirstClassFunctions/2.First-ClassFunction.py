# Python supports first-class functions because it treats its functions like
# first class objects.
# So it can perform following tasks.:
# 1. A function is an instance of the object type
# 2. can store the function in a variable
# 3. can pass one function as a parameter of another function
# 4. can return the function from a function
# 5. you can store them in a data structure such as hash tables, lists etc.
