# A first-class object is an entity in a programming language, that can:
# 1. be created
# 2. be destroyed
# 3. Appear in an expression
# 4. be assigned to a variable
# 5. be used as an argument
# 6. be return returned by a function call
# 
# To help remember what first-class objects are, think of the acronym 'EVAC'. 
# First-class objects can be used in Expressions, as Variables, and as Arguments,
# and can be returned by function Calls. 
# 
# Note:
# primitive data types, such as integers and strings, as first-class objects in the 
# procedural paradigm. 
# In the functional programming paradigm, functions are first-class objects.